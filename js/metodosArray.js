// Se ocupa para unir uno o mas arrays 
// Este método, no cambia los metodos excistentes, si no que devuelve un array nuevo. 

let alumnosSalon1 = ['Carlos', 'Tomas', 'Diego', 'Lisbeth']
console.log('Los alumnos del primer salón son: ', alumnosSalon1)

let alumnosSalon2 = ['Pedrito', 'Juan', 'Francisco']
console.log('Los alumnos del Salón 2 son: ', alumnosSalon2)

let alumnosTotal = alumnosSalon1.concat(alumnosSalon2)

console.log('El Total de los alumnos esta conformado por: ', alumnosTotal)





